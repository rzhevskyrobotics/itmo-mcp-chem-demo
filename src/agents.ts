export type StageKind =
  | 'intake'
  | 'agent'
  | 'installation';

export interface StageDef {
  id: number;
  key: string;
  module: string;
  agent: string;
  does: string;
  output: string;
  kind: StageKind;

  fallback: (ctx: FallbackContext) => string;
}

export interface FallbackContext {
  request: string;
  product: string;
  properties: string;
  application: string;
  constraints: string;
  scale: string;

  experiment?: {
    flowA: number; flowB: number; reactorTemp: number;
    sampleCount: number; lastConc: number; avgConc: number;
    pressureMin: number; pressureMax: number; durationSec: number; finalStatus: string;
  };
}

